// WAP to validate that the addition of 2 numbers is equals to the given number?

package sample1;

public class Add2num {
	public static void main(String args[])
	{
     int num1=10;
     int num2=20;
     int Given_Num=30;
     int sum=num1 + num2;
     
     if (sum==Given_Num)
     {
    	 System.out.println("sum of 2 numbers is equal to the given number");
     }else
     {
    	 System.out.println("sum of 2 numbers is not equal to the given number");
     }
	}
	
}

